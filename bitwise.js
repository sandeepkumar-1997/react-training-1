let x = 5 & 1;          // Bitwise AND
console.log(x);
x = 5 | 1;              // Bitwise OR
console.log(x);
x = 5 ^ 1;              // Bitwise XOR
console.log(x);
x = ~5;                 // Bitwise NOT
console.log(x);
x = 5 << 2;             // Bitwise Left Shift
console.log(x);
x = -5 >> 1;            // Bitwise Right Shift
console.log(x);
